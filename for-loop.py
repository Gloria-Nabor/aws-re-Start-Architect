# Trabajando con un bucle while

# La instrucción for utiliza las palabras clave for … in para indicar recorra la lista. 
# La función range() genera una lista. toma un número inicial y un número final, pero el número final no está incluido. 
# Por tanto, pasa 11 para que la función deje de contar en 10

print("Count to 10!")
for x in range (0, 11):
    print(x)